def get_structured_persona(persona_id):
    """Return the structured persona with the given id, or None."""
    for p in STRUCTURED_PERSONAS:
        if p["id"] == persona_id:
            return p
    return None


def get_all_structured_personas():
    """Return all 20 structured personas in shuffled order (10 male, 10 female)."""
    return list(STRUCTURED_PERSONAS)
